# ── Argument parsing ──────────────────────────────────────────────────────────

def get_arguments():
    """Read SLICE_DURATION from openoptics_config.json (already loaded as _cfg).

    Sets globals:
        slice_duration_us (int): slice length in microseconds
        max_qdepth (int): max bytes a single port-queue can buffer in one slice
                          at LINK_GBPS, minus GUARDBAND.
    """
    global slice_duration_us, max_qdepth

    slice_duration_us = _cfg["SLICE_DURATION"]
    # Bytes one port can transmit in one slice at LINK_GBPS:
    #   slice_ns × Gbps / 8 = (slice_us × 1000) × LINK_GBPS / 8
    slice_duration_ns = slice_duration_us * 1000
    max_qdepth = int(slice_duration_ns * LINK_GBPS / 8) - GUARDBAND


